/**
 * Print the sum and product of the integers between
 * 13 and 17, inclusive.
 *
 * @Umasathvik Pantham 
 * @02/06/2021
 */
public class ArithmeticPrinter
{
    public static void main(String[] args)
    {
       System.out.print("The sum of 13 + 14 + 15 + 16 + 17 is ");
       System.out.print(13 + 14 + 15 + 16 + 17);
       System.out.println(".");
       
       System.out.println();
       
       System.out.print("The product of 13 * 14 * 15 * 16 * 17 is ");
       System.out.print(13 * 14 * 15 * 16 * 17);
       System.out.println(".");
    }
}